d = int(input('Dia? '))

if d == 1:
    print('domingo')
else:
    if d == 2:
        print('segunda')
    else:
        if d == 3:
            print('terça')
        else:
            if d == 4:
                print('quarta')
            else:
                if d == 5:
                    print('quinta')
                else:
                    if d == 6:
                        print('sexta')
                    else:
                        if d == 7:
                            print('sábado')
                        else:
                            print('dia inválido')
